def hodge_diamond_torus(complex_dim):
    """
    Hodge numbers h^{p,q} for the complex n-torus T^{2n}.
    h^{p,q}(T^{2n}) = C(n,p) * C(n,q)  where C is binomial coefficient.
    All forms are harmonic. All (p,p) classes are algebraic.
    """
    from math import comb
    n = complex_dim
    diamond = {}
    for p in range(n+1):
        for q in range(n+1):
            diamond[(p,q)] = comb(n,p) * comb(n,q)
    return diamond


def hodge_diamond_projective_space(n):
    """
    Hodge numbers h^{p,q}(P^n).
    h^{p,q} = 1 if p == q and 0 <= p <= n, else 0.
    All (p,p) classes are algebraic (powers of the hyperplane class H).
    """
    diamond = {}
    for p in range(n+1):
        for q in range(n+1):
            diamond[(p,q)] = 1 if p == q else 0
    return diamond


def hodge_diamond_k3():
    """
    Hodge numbers of the K3 surface (complex dim 2).
    h^{0,0}=1, h^{1,0}=0, h^{0,1}=0, h^{1,1}=20, h^{2,0}=1, h^{0,2}=1, h^{2,2}=1.
    The 20-dimensional H^{1,1} has a 2D algebraic part (from elliptic fibration).
    This is the key case where Hodge != algebraic in general.
    """
    return {(0,0):1, (1,0):0, (0,1):0, (1,1):20,
            (2,0):1, (0,2):1, (2,1):0, (1,2):0, (2,2):1}


def print_hodge_diamond(diamond, n, name):
    """Print Hodge diamond and identify (p,p) classes."""
    print(f"\n  Hodge diamond of {name} (complex dim {n}):")
    for p in range(n+1):
        row = [diamond.get((p,q), 0) for q in range(n+1)]
        print(f"    p={p}: " + "  ".join(f"{v:4d}" for v in row))


def pp_classes(diamond, n):
    """Return the (p,p) Hodge numbers."""
    return {p: diamond.get((p,p), 0) for p in range(n+1)}


